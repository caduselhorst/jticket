    private void carregaTabela() {
        if(jComboBox1.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Informe um tipo de filtro", "Informação", JOptionPane.INFORMATION_MESSAGE);
            jComboBox1.requestFocus();
        } else {
            List<Produto> produtos = null;
            switch(jComboBox1.getSelectedIndex()) {
                case 0: {
                    if(jTextField1.getText().isEmpty()) {
                        JOptionPane.showMessageDialog(this, "Informe um código", "Informação", JOptionPane.INFORMATION_MESSAGE);
                        jTextField1.requestFocus();
                    } else {
                        try {
                            produtos = service.carregaProdutos(
                                    FiltroProduto.builder()
                                            .codigo(Long.parseLong(jTextField1.getText()))
                                            .build());
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(this, "Informe um número válido", "Informação", JOptionPane.INFORMATION_MESSAGE);
                            jTextField1.requestFocus();
                            jTextField1.selectAll();
                        }
                    }
                    
                    break;
                }
                default: {
                    produtos = service.carregaProdutos(
                                    FiltroProduto.builder()
                                            .descricao(jTextField1.getText())
                                            .build());
                    break;
                }
            }
            
            ((ProdutoTableModel) jTable1.getModel()).setProdutos(produtos);
            TableUtils.redimensionaColunas(jTable1);
        }
    }

