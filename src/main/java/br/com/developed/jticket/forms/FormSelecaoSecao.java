    private void carregaTabela() {
        if(departamento == null) {
            ((SecaoTableModel) jTable1.getModel()).setSecoes(secaoService.carregaTodas());
        } else {
            ((SecaoTableModel) jTable1.getModel()).setSecoes(secaoService.carregaPorDepartamento(departamento));
        }
        TableUtils.redimensionaColunas(jTable1);
    }
    
